#!/usr/bin/env node
// Read pages.json + shared-components.json and emit:
//   - src/layouts/Base.astro
//   - src/components/<Name>.astro  (one per detected shared component)
//   - src/pages/<route>.astro      (one per page)
//
// Strategy: keep the page's body HTML verbatim. For each shared component
// candidate, replace its serialised HTML inside the body with an Astro
// component reference. This way visual parity is preserved.
//
// Usage: node 05-emit-astro.mjs <astro-dir>

import { promises as fs } from 'node:fs';
import path from 'node:path';

const [, , AST] = process.argv;
if (!AST) {
  console.error('usage: node 05-emit-astro.mjs <astro-dir>');
  process.exit(2);
}
const astAbs = path.resolve(AST);
const extractDir = path.join(astAbs, '.framer-extract');
const layoutsDir = path.join(astAbs, 'src', 'layouts');
const componentsDir = path.join(astAbs, 'src', 'components');
const pagesDir = path.join(astAbs, 'src', 'pages');

await fs.mkdir(layoutsDir, { recursive: true });
await fs.mkdir(componentsDir, { recursive: true });
await fs.mkdir(pagesDir, { recursive: true });

const pages = JSON.parse(await fs.readFile(path.join(extractDir, 'pages.json'), 'utf8'));
let shared = [];
try {
  shared = JSON.parse(await fs.readFile(path.join(extractDir, 'shared-components.json'), 'utf8'));
} catch {
  console.warn('[emit] no shared-components.json — skipping component extraction');
}

// ── Helpers ─────────────────────────────────────────────────────────────
function attrsToString(attrs) {
  return Object.entries(attrs || {})
    .map(([k, v]) => v === '' ? k : `${k}="${escapeAttr(String(v))}"`)
    .join(' ');
}
function escapeAttr(s) {
  return s.replace(/"/g, '&quot;');
}
// Astro template literals choke on `${...}` and backticks in raw HTML.
// We use Fragment with set:html instead — much safer for opaque blobs.
// (No escaping needed; set:html accepts raw string.)

// Route → file path under src/pages/
//   /                 → index.astro
//   /blog             → blog.astro
//   /blog/some-post   → blog/some-post.astro
//   /404              → 404.astro (Astro's special 404 handler)
function routeToPagePath(route) {
  if (route === '/' || route === '') return 'index.astro';
  const clean = route.replace(/^\/+/, '').replace(/\/+$/, '');
  // /404 must land at src/pages/404.astro for Astro to use it as the 404 handler.
  // (A nested /blog/404 still goes to blog/404.astro, which is fine.)
  return clean + '.astro';
}

// Sanitise a fragment for embedding inside Astro: the only thing we MUST escape
// is the literal backtick (since we'll embed via set:html on a JS string).
// We pass the string from frontmatter so we use JSON.stringify for safety.
function jsString(s) {
  return JSON.stringify(s);
}

// ── 1. Emit shared components ────────────────────────────────────────────
const componentByHash = new Map(); // hash → { name, html }
for (const c of shared) {
  // Avoid name collisions
  let name = c.suggestedComponentName;
  let suffix = 2;
  while ([...componentByHash.values()].some((v) => v.name === name)) {
    name = c.suggestedComponentName + suffix++;
  }
  componentByHash.set(c.hash, { name, html: c.sampleHtml });

  const file = path.join(componentsDir, `${name}.astro`);
  const body = `---
// Auto-generated from data-framer-name="${c.framerName}".
// Detected on routes: ${c.occursOnRoutes.join(', ')}.
const HTML = ${jsString(c.html)};
---
<Fragment set:html={HTML} />
`;
  await fs.writeFile(file, body);
  console.log(`[emit] component → ${path.relative(astAbs, file)}`);
}

// ── 2. Emit Base.astro layout ────────────────────────────────────────────
// The layout takes everything that's *per-page* via props, and renders
// the shell (<html>/<head>/<body>) around them.
//
// Scripts are rendered via Fragment set:html as raw <script> markup —
// Astro's <script> component tries to bundle through Vite, and Vite's
// esbuild stage chokes on certain Framer inline scripts. Raw HTML bypasses
// this entirely, which is exactly what we want for opaque vendor scripts.
const baseAstro = `---
// Auto-generated by framer-to-astro skill.
type ScriptTag = { src?: string; type?: string; async?: string; defer?: string; _inner?: string; [k: string]: any };
type LinkTag = { rel?: string; href?: string; [k: string]: any };
type MetaTag = { name?: string; property?: string; content?: string; charset?: string; [k: string]: any };
type StyleBlock = { attrs: Record<string, string>; css: string };

interface Props {
  title: string;
  htmlAttrs?: Record<string, string>;
  bodyAttrs?: Record<string, string>;
  metaTags?: MetaTag[];
  linkTags?: LinkTag[];
  scriptTags?: ScriptTag[];
  inlineStyles?: StyleBlock[];
}

const {
  title,
  htmlAttrs = {},
  bodyAttrs = {},
  metaTags = [],
  linkTags = [],
  scriptTags = [],
  inlineStyles = [],
} = Astro.props;

// Render every script tag as raw HTML so Astro/Vite never tries to parse it.
function renderScriptTag(s: ScriptTag): string {
  const { _inner = '', ...attrs } = s;
  const attrStr = Object.entries(attrs)
    .map(([k, v]) => v === '' ? k : k + '="' + String(v).replace(/"/g, '&quot;') + '"')
    .join(' ');
  if (attrs.src) {
    return '<script ' + attrStr + '></script>';
  }
  return '<script ' + attrStr + '>' + _inner + '</script>';
}
const allScriptsHtml = scriptTags.map(renderScriptTag).join('\\n    ');
---
<!DOCTYPE html>
<html {...htmlAttrs}>
  <head>
    {/* Charset must be in the first 1024 bytes per HTML spec — emit FIRST.
        Then title. Then everything else. */}
    {metaTags.filter((m) => m.charset).map((m) => <meta {...m} />)}
    <title>{title}</title>
    {metaTags.filter((m) => !m.charset).map((m) => <meta {...m} />)}
    {linkTags.map((l) => <link {...l} />)}
    {inlineStyles.map((s) => <style {...s.attrs} set:html={s.css} is:inline />)}
  </head>
  <body {...bodyAttrs}>
    <slot />
    <Fragment set:html={allScriptsHtml} />
  </body>
</html>
`;
await fs.writeFile(path.join(layoutsDir, 'Base.astro'), baseAstro);
console.log(`[emit] layout → src/layouts/Base.astro`);

// ── 3. Emit one page per route ───────────────────────────────────────────
function replaceSharedRegions(bodyHtml, _route) {
  if (componentByHash.size === 0) return { html: bodyHtml, used: [] };

  // Build a regex for each component's sample HTML. Since extraction
  // captured byte-identical HTML, we replace literal occurrences.
  // We escape regex metachars and replace first occurrence only.
  let html = bodyHtml;
  const used = [];
  for (const [, { name, html: sample }] of componentByHash) {
    const idx = html.indexOf(sample);
    if (idx === -1) continue;
    html = html.slice(0, idx) + `<!-- @@FRAMER_TO_ASTRO_COMPONENT:${name}@@ -->` + html.slice(idx + sample.length);
    used.push(name);
  }
  return { html, used };
}

for (const page of pages) {
  const { html: bodyWithMarkers, used } = replaceSharedRegions(page.hydrateMain, page.route);

  // Astro page front-matter passes data into Base.astro
  const importLines = used.map((n) => `import ${n} from '../components/${n}.astro';`).join('\n');
  // We turn the markers back into <Component /> tags via a post-process step:
  // since set:html cannot render Astro components, we emit two <Fragment set:html>
  // chunks around each component reference.

  // Split the body into segments alternating: html, component, html, component...
  const segments = [];
  let remaining = bodyWithMarkers;
  const markerRe = /<!-- @@FRAMER_TO_ASTRO_COMPONENT:([A-Za-z0-9_]+)@@ -->/;
  while (true) {
    const m = markerRe.exec(remaining);
    if (!m) {
      segments.push({ type: 'html', value: remaining });
      break;
    }
    segments.push({ type: 'html', value: remaining.slice(0, m.index) });
    segments.push({ type: 'component', value: m[1] });
    remaining = remaining.slice(m.index + m[0].length);
  }

  // Emit page
  const pagePath = path.join(pagesDir, routeToPagePath(page.route));
  await fs.mkdir(path.dirname(pagePath), { recursive: true });

  const relImport = (target) => {
    let rel = path.relative(path.dirname(pagePath), target).split(path.sep).join('/');
    if (!rel.startsWith('.')) rel = './' + rel;
    return rel;
  };
  const frontmatter = `---
import Base from '${relImport(path.join(astAbs, 'src/layouts/Base.astro'))}';
${used.length ? used.map((n) => `import ${n} from '${relImport(path.join(astAbs, 'src/components', n + '.astro'))}';`).join('\n') : ''}

const PAGE = ${JSON.stringify({
    title: page.title,
    htmlAttrs: page.htmlAttrs,
    bodyAttrs: page.bodyAttrs,
    metaTags: page.metaTags,
    linkTags: page.linkTags,
    scriptTags: page.scriptTags,
    inlineStyles: page.inlineStyles,
  }, null, 2)};

const SEGMENTS = ${JSON.stringify(segments)};
---
<Base {...PAGE}>
  {SEGMENTS.map((s) => (
    s.type === 'html'
      ? <Fragment set:html={s.value} />
      : ${used.length
          ? '(' + used.map((n) => `s.value === ${jsString(n)} ? <${n} />`).join(' : ') + ' : null)'
          : 'null'}
  ))}
</Base>
`;

  await fs.writeFile(pagePath, frontmatter);
  console.log(`[emit] page → ${path.relative(astAbs, pagePath)}  (${used.length} component refs)`);
}

console.log(`[emit] done. ${pages.length} pages, ${componentByHash.size} shared components.`);
